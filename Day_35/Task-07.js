//Odd number by While loop
var number = 1;

while (number < 9) {
    console.log(number);
    number+=2;
}

//Even Number by While Loop
var number = 0;

while (number < 9) {
    console.log(number);
    number+=2;
}

//Even number by for loop
for (let i = 0; i <= 19; i+=2) {
    console.log(i);
}

//Odd number by for loop
for (let i = 1; i <= 19; i+=2) {
    console.log(i);
}